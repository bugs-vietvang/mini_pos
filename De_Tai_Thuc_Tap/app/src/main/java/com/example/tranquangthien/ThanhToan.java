package com.example.tranquangthien;

public class ThanhToan {

    private int maThanhToan, soLuong;
    private String tenMonAn, giaTien, thanhTien, anhMonAn;

    public ThanhToan() {
    }

    public int getMaThanhToan() {
        return maThanhToan;
    }

    public void setMaThanhToan(int maThanhToan) {
        this.maThanhToan = maThanhToan;
    }

    public int getSoLuong() {
        return soLuong;
    }

    public void setSoLuong(int soLuong) {
        this.soLuong = soLuong;
    }

    public String getTenMonAn() {
        return tenMonAn;
    }

    public void setTenMonAn(String tenMonAn) {
        this.tenMonAn = tenMonAn;
    }

    public String getGiaTien() {
        return giaTien;
    }

    public void setGiaTien(String giaTien) {
        this.giaTien = giaTien;
    }

    public String getThanhTien() {
        return thanhTien;
    }

    public void setThanhTien(String thanhTien) {
        this.thanhTien = thanhTien;
    }

    public String getAnhMonAn() {
        return anhMonAn;
    }

    public void setAnhMonAn(String anhMonAn) {
        this.anhMonAn = anhMonAn;
    }

}
